package christmas.service;

import christmas.domain.OrderMenu;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import java.util.Arrays;
import java.util.List;
import static org.junit.jupiter.api.Assertions.assertEquals;

public class OrderCalculatorTest {

    @DisplayName("총주문 금액은 할인 전 고객이 원래 지불할 금액이다.")
    @Test
    public void calculateTotalPriceTest() {
        // Arrange
        List<OrderMenu> orderList = Arrays.asList(
                new OrderMenu("양송이수프", 2),
                new OrderMenu("타파스", 1),
                new OrderMenu("티본스테이크", 3)
                // Add more OrderMenu instances as needed
        );

        int totalAmount = OrderCalculator.calculateTotalPrice(orderList);

        int expectedTotalAmount = 2 * 6000 + 1 * 5500 + 3 * 55000;
        assertEquals(expectedTotalAmount, totalAmount);
    }
}
